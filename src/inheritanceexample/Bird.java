package inheritanceexample;

public class Bird extends Animal {
    public void fly(){
        System.out.println("Birds can fly.");
    }
    public void see(){
        System.out.println("Bird can see.");
    }
    public void smell(){
        System.out.println("Birds can smell.");
    }
    public void breathe(){
        System.out.println("Birds can breathe.");
    }
}
