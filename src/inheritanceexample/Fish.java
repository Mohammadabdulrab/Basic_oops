package inheritanceexample;

public class Fish extends Animal {
    public void swim(){
        System.out.println("Fish can swim.");
    }
    public void see(){
        System.out.println("fish can see.");
    }
    public void smell(){
        System.out.println("Fish can smell.");
    }
    public void breathe(){
        System.out.println("Fish can breathe.");
    }
}
